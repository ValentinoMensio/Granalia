def table_exists(inspector: sa.Inspector, table_name: str) -> bool:
    return table_name in inspector.get_table_names(schema="public")


def column_exists(inspector: sa.Inspector, table_name: str, column_name: str) -> bool:
    if not table_exists(inspector, table_name):
        return False
    return column_name in {column["name"] for column in inspector.get_columns(table_name, schema="public")}


