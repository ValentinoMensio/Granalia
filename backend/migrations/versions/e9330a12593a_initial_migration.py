def normalize_existing_schema(inspector: sa.Inspector) -> None:
    bind = op.get_bind()

    if table_exists(inspector, "discount_policies"):
        op.execute("DROP TABLE discount_policies")
    if table_exists(inspector, "client_profiles"):
        op.execute("DROP TABLE client_profiles")

    for table_name, column_name in [
        ("customers", "customer_key"),
        ("customers", "discount_policy_id"),
        ("customers", "mode"),
        ("customers", "line_discount_label"),
        ("customers", "transport"),
        ("products", "code"),
        ("product_offerings", "code"),
        ("product_offerings", "format_class"),
        ("invoices", "discount_policy_id"),
        ("invoices", "mode"),
    ]:
        if column_exists(inspector, table_name, column_name):
            op.execute(f'ALTER TABLE {table_name} DROP COLUMN IF EXISTS {column_name}')

    if table_exists(inspector, "invoices") and not column_exists(inspector, "invoices", "transport_id"):
        op.execute("ALTER TABLE invoices ADD COLUMN transport_id BIGINT")

    if table_exists(inspector, "customers") and column_exists(inspector, "customers", "transport"):
        rows = bind.execute(sa.text("SELECT id, transport, transport_id FROM customers ORDER BY id")).mappings().all()
        for row in rows:
            if row.get("transport_id") or not str(row.get("transport") or "").strip():
                continue
            transport_name = str(row["transport"]).strip()
            bind.execute(
                sa.text(
                    """
                    INSERT INTO transports (name, notes, created_at, updated_at)
                    VALUES (:name, '[]'::jsonb, now(), now())
                    ON CONFLICT (name) DO UPDATE SET updated_at = excluded.updated_at
                    """
                ),
                {"name": transport_name},
            )
            transport_id = bind.execute(
                sa.text("SELECT transport_id FROM transports WHERE name = :name"),
                {"name": transport_name},
            ).scalar_one()
            bind.execute(
                sa.text("UPDATE customers SET transport_id = :transport_id, updated_at = now() WHERE id = :id"),
                {"transport_id": transport_id, "id": row["id"]},
            )

    if table_exists(inspector, "invoices") and column_exists(inspector, "invoices", "transport"):
        rows = bind.execute(sa.text("SELECT id, transport, transport_id FROM invoices ORDER BY id")).mappings().all()
        for row in rows:
            if row.get("transport_id") or not str(row.get("transport") or "").strip():
                continue
            transport_name = str(row["transport"]).strip()
            bind.execute(
                sa.text(
                    """
                    INSERT INTO transports (name, notes, created_at, updated_at)
                    VALUES (:name, '[]'::jsonb, now(), now())
                    ON CONFLICT (name) DO UPDATE SET updated_at = excluded.updated_at
                    """
                ),
                {"name": transport_name},
            )
            transport_id = bind.execute(
                sa.text("SELECT transport_id FROM transports WHERE name = :name"),
                {"name": transport_name},
            ).scalar_one()
            bind.execute(
                sa.text("UPDATE invoices SET transport_id = :transport_id WHERE id = :id"),
                {"transport_id": transport_id, "id": row["id"]},
            )

    if table_exists(inspector, "catalogs"):
        catalog_rows = bind.execute(sa.text("SELECT id, catalog FROM catalogs ORDER BY id")).mappings().all()
        product_rows = bind.execute(sa.text("SELECT id, name FROM products")).mappings().all() if table_exists(inspector, "products") else []
        offering_rows = bind.execute(sa.text("SELECT id, product_id, label FROM product_offerings")).mappings().all() if table_exists(inspector, "product_offerings") else []
        product_by_name = {row["name"]: row["id"] for row in product_rows}
        offering_by_key = {(row["product_id"], row["label"]): row["id"] for row in offering_rows}
        for row in catalog_rows:
            normalized_catalog = []
            for product in row["catalog"] or []:
                product_name = product.get("name", "")
                product_id = product_by_name.get(product_name, product.get("id"))
                normalized_product = {
                    "id": product_id,
                    "name": product_name,
                    "aliases": product.get("aliases", []),
                    "offerings": [],
                }
                for offering in product.get("offerings", []):
                    offering_label = offering.get("label", "")
                    offering_id = offering_by_key.get((product_id, offering_label), offering.get("id"))
                    normalized_product["offerings"].append(
                        {
                            "id": offering_id,
                            "label": offering_label,
                            "price": int(offering.get("price", 0)),
                        }
                    )
                normalized_catalog.append(normalized_product)
            bind.execute(
                sa.text("UPDATE catalogs SET catalog = :catalog WHERE id = :id"),
                {"catalog": normalized_catalog, "id": row["id"]},
            )


